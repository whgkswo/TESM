package net.whgkswo.tesm.gui.component.box;

public class Box {
    private double xRatio;
    private double yRatio;

    public Box(double xRatio, double yRatio) {
        this.xRatio = xRatio;
        this.yRatio = yRatio;
    }

    public double getxRatio() {
        return xRatio;
    }

    public double getyRatio() {
        return yRatio;
    }
}
